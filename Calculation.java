
public class Calculation {
	public static void main(String[] args) {
		int a = 15;
		int c;
		c = a + 3;
		System.out.println(c);

		a++;
		System.out.println(a);
		a--;
		System.out.println(a);
		a += 5;
		System.out.println(a);
		int b = 5;
		boolean d;
		d = b < 5;
		System.out.println(d);
	}
}
